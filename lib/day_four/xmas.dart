  calculateAppearances() {
    List<List<String>> input = Utils.stringToEmbeddedList(value);

//     List<List<String>> input = Utils.stringToEmbeddedList("""MMMSXXMASM
// MSAMXMSMSA
// AMXSXMAAMM
// MSAMASMSMX
// XMASAMXAMM
// XXAMMXXAMA
// SMSMSASXSS
// SAXAMASAAA
// MAMMMXMMMM
// MXMXAXMASX""");

    for (var i = 0; i < input.length; i++) {
      for (var y = 0; y < input[i].length; y++) {
        String letter = input[i][y];
        if (letter == "X") {
          // horizontal
          if (isWithinBounds(y + 3, 0, input[i].length) &&
              check([letter, input[i][y + 1], input[i][y + 2], input[i][y + 3]]
                  .join())) {
            appearances += 1;
          }
          // horizontal backward
          if (isWithinBounds(y - 3, 0, input[i].length) &&
              check([letter, input[i][y - 1], input[i][y - 2], input[i][y - 3]]
                  .join())) {
            appearances += 1;
          }
          // vertical
          if (isWithinBounds(i + 3, 0, input.length) &&
              check([letter, input[i + 1][y], input[i + 2][y], input[i + 3][y]]
                  .join())) {
            appearances += 1;
          }
          // vertical backward
          if (isWithinBounds(i - 3, 0, input.length) &&
              check([letter, input[i - 1][y], input[i - 2][y], input[i - 3][y]]
                  .join())) {
            appearances += 1;
          }
          //diagonals

          if (isWithinBounds(i + 3, 0, input.length) &&
              isWithinBounds(y + 3, 0, input.length)) {
            if (check([
              letter,
              input[i + 1][y + 1],
              input[i + 2][y + 2],
              input[i + 3][y + 3]
            ].join())) {
              appearances += 1;
            }
          }
          if (isWithinBounds(i - 3, 0, input.length) &&
              isWithinBounds(y - 3, 0, input.length)) {
            if (check([
              letter,
              input[i - 1][y - 1],
              input[i - 2][y - 2],
              input[i - 3][y - 3]
            ].join())) {
              appearances += 1;
            }
          }

          if (isWithinBounds(i - 3, 0, input.length) &&
              isWithinBounds(y + 3, 0, input.length)) {
            if (check([
              letter,
              input[i - 1][y + 1],
              input[i - 2][y + 2],
              input[i - 3][y + 3]
            ].join())) {
              appearances += 1;
            }
          }

          if (isWithinBounds(i + 3, 0, input.length) &&
              isWithinBounds(y - 3, 0, input.length)) {
            if (check([
              letter,
              input[i + 1][y - 1],
              input[i + 2][y - 2],
              input[i + 3][y - 3]
            ].join())) {
              appearances += 1;
            }
          }
        }
      }
    }
  }

  bool isWithinBounds(int index, int lowerBound, int upperBound) {
    return index >= lowerBound && index < upperBound;
  }

  bool check(String v) {
    if (v == "XMAS") return true;
    return false;
  }
